#include <iostream>
#include <cmath>

int main() {
    std::cout << "If a rectangle has the dimensions:" << std::endl;
    std::cout << "5cm x 3cm" << std::endl;
    std::cout << std::endl;
    std::cout << "Area is " << 5*3 << "cm^2" << std::endl;
    std::cout << "Perimeter is " << 2*(5+3) << "cm" << std::endl;
}
